    public float mySixthVariable; // 4 bytes // 4F
    public long mySeventhVariable; // 8 bytes //1L
    public double myEighthVariable; // 8 bytes //1.1

    //methods

    public void myFirstMethod() {

    }

    public int mySecondMethod() {
        return 0;
    }

    public float myThirdMethod() {
        return 1F;
    }

