/**
 *  Variables: Containers for storing data values
 *
 *  SYNTAX: <access-specifier> <data-type> variableName
 *  [access-type] - public, private, protected, default
 *  [data-type]   - byte, short, char, bool, int, long, double, float
 *
 */
